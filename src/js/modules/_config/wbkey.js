/*
 * Given the page name from the url, return an index into
 * workbook.json. The index contains three parts; part, section, page.
 */
export function getIndexFromLesson(lesson) {
  let code = wbl[lesson];

  if (!code) {
    throw new Error(`Can't find workbook mapping for lesson ${lesson}`);
  }

  var index = code.split(":");

  return {
    part: parseInt(index[0], 10),
    section: parseInt(index[1], 10),
    page: parseInt(index[2], 10)
  };
}

